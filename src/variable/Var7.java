package variable;

public class Var7 {

    static void main() {
        int a = 100;//좌변 변수, 우변 리터럴이라고 부른다.
        double b = 10.5;//실수 리터럴
        boolean c = true;//불리언 리터럴
        char d = 'A';//따옴표 하나.
        String e = "Hello Java";//따옴표 둘
        //리터럴은 개발자가 직접 입력한 고정된 값. 변하지 않는다

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);



    }
}
